#!/usr/bin/env python3
""" This return concatenated string"""
def concat(str1:str, str2:str) -> str:
  """It takes a two string and return
      concatenated string
  Args:
      str1 (str): the first string
      str2 (str): the second string
  Returns:
      str: this return the concatenated string
  """
  return str1 + str2
